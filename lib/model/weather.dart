@JsonSerializable()
class Weather {
  final Location? location;
  final Current? current;
  final Forecast? forecast;

  Weather({
    this.location,
    this.current,
    this.forecast,
  });
  factory Weather.fromJson(Map<String,dynamic> json) => _$WeatherFromJson(json);
  Map<String,dynamic> toJson()=> _$WeatherToJson(this);

}
